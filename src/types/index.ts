/**
 * Типы данных для мифологического портала
 * 
 * Определяют структуру данных для всех сущностей проекта:
 * - Культуры (мифологии разных народов)
 * - Сущности (боги, герои, существа)
 * - Мифы и легенды
 * - Типы сущностей
 */

// ============================================================================
// Основные сущности
// ============================================================================

/**
 * Культура/Мифология — представляет мифологическую систему определённой культуры
 * Примеры: греческая, египетская, скандинавская мифологии
 */
export interface Culture {
  /** Уникальный идентификатор культуры */
  id: number;
  /** URL-слаг для использования в роутинге (например, "greek", "egypt") */
  slug: string;
  /** Название культуры (например, "Греческая мифология") */
  title: string;
  /** HTML-описание культуры */
  description: string;
  /** Статус публикации: "published", "draft" и т.д. */
  status: string;
  /** Порядок сортировки в списках */
  sort?: number | null;
  /** Дата создания записи */
  date_created?: string;
}

/**
 * Тип сущности — классификатор для мифологических персонажей
 * Примеры: Бог, Герой, Существо, Титан
 */
export interface EntityType {
  /** Уникальный идентификатор типа */
  id: number;
  /** URL-слаг типа (например, "god", "hero") */
  slug: string;
  /** Название типа (например, "Бог") */
  title: string;
  /** HTML-описание типа */
  description: string;
  /** Статус публикации */
  status: string;
  /** Порядок сортировки */
  sort?: number | null;
  /** Основное изображение типа */
  main_img?: string | null;
}

/**
 * Сущность — базовая модель для всех мифологических персонажей
 * Используется для богов, героев, существ и других персонажей
 * Все данные хранятся в коллекции Directus "entities"
 */
export interface Entity {
  /** Уникальный идентификатор сущности */
  id: number;
  /** URL-слаг для роутинга (например, "zevs", "aid") */
  slug: string;
  /** Имя/название сущности */
  title: string;
  /** Краткое описание (excerpt) — основной текст описания */
  excerpt?: string | null;
  /** Полное HTML-описание (может быть null, если используется excerpt) */
  description?: string | null;
  /** Статус публикации */
  status: string;
  /** Порядок сортировки */
  sort?: number | null;
  /** Дата создания записи */
  date_created?: string;
  /** ID основного изображения в Directus Files */
  main_image?: string | null;
  /** ID типа сущности (1=Бог, 2=Герой, 4=Существо, 6=Титан) */
  entity_type: number;
  /** ID связанной культуры/мифологии */
  mythology: number;
  /** Пол: "male" | "female" */
  gender?: 'male' | 'female' | null;
  /** Принадлежность/роль (например, "Бог смерти и подземного мира") */
  belonging?: string | null;
  /** Первое упоминание в источниках */
  first_mention?: string | null;
  /** Транскрипция имени (например, "Ἀΐδης") */
  transcript?: string | null;
  /** Массив ID изображений галереи */
  gallery?: string[];
  /** Массив ID родительских сущностей */
  parents?: number[];
  /** Массив ID брачных союзов */
  marriages?: number[];
  /** SEO заголовок */
  seo_title?: string | null;
  /** SEO описание */
  seo_description?: string | null;
  /** Теги для категоризации */
  tags?: string[] | null;
}

/**
 * Миф/Легенда — история из мифологии
 * Данные хранятся в коллекции Directus "myths_and_legends"
 */
export interface Myth {
  /** Уникальный идентификатор мифа */
  id: number;
  /** URL-слаг для роутинга */
  slug: string;
  /** Название мифа */
  title: string;
  /** Краткое описание/превью */
  prev_text: string;
  /** Полный HTML-текст мифа */
  content: string;
  /** Статус публикации */
  status: string;
  /** Порядок сортировки */
  sort?: number | null;
  /** Дата создания */
  date_created?: string;
  /** Дата последнего обновления */
  date_updated?: string | null;
  /** ID связанной культуры */
  culture: number;
  /** Массив ID изображений */
  images?: string[];
  /** Основное изображение (устаревшее поле) */
  img?: string | null;
}

// ============================================================================
// API ответы
// ============================================================================

/**
 * Стандартный ответ API для коллекций (массив объектов)
 * @template T — тип данных в массиве
 */
export interface ApiResponse<T> {
  /** Массив данных */
  data: T[];
}

/**
 * Ответ API для одиночного объекта
 * @template T — тип данных
 */
export interface ApiSingleResponse<T> {
  /** Одиночный объект данных */
  data: T;
}

// ============================================================================
// Параметры запросов
// ============================================================================

/**
 * Параметры для фильтрации и пагинации API запросов
 */
export interface QueryParams {
  /** Фильтр по культуре (slug) */
  culture?: string;
  /** Фильтр по участнику (slug) */
  participant?: string;
  /** Лимит записей */
  limit?: number;
  /** Смещение (offset) */
  offset?: number;
  /** Сортировка */
  sort?: string;
}
